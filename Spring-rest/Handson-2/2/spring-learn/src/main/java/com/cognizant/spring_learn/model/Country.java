package com.cognizant.spring_learn.model;

public class Country {
    private String code;
    private  String name;

    public void setCode(String code) {
        this.code = code;
    }

    public String getCode(){
        return this.code;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName(){
        return this.name;
    }
}
